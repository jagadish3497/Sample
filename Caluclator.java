package com.jagadish.ds;

public class Caluclator {
	int num1,num2,result;
	void add(int num1,int num2){
		result=num1+num2;
		System.out.println(result);
	}
	void sub(int num1,int num2){
		result=num1-num2;
		System.out.println(result);
	}
	void div(int num1,int num2){
		result=num1/num2;
		System.out.println(result);
	}
	void mul(int num1,int num2){
		result=num1*num2;
		System.out.println(result);
	}

}
